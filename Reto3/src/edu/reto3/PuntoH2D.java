public class PuntoH2D {
    private double _x;
    private double _y;
    private double _w;

    public PuntoH2D(double x, double y, double w) {
        _x = x;
        _y = y;
        _w = w;

    }

    public double get_x() {
        return _x;
    }

    public double get_y() {
        return _y;
    }

    public double get_w() { return _w;}
}
