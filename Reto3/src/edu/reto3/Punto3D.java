public class Punto3D {

    private double _x;
    private double _y;
    private double _z;

    public Punto3D(double x, double y, double z) {
        _x = x;
        _y = y;
        _z = z;
    }

    public double get_x() {
        return _x;
    }

    public double get_y() {
        return _y;
    }

    public double get_z() {
        return _z;
    }
}
