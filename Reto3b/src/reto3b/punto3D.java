package reto3b;

/**
 *
 * @author lope
 */
public class punto3D {
    
    private double x;
    private double y;
    private double z;
    
    public punto3D(double x1, double y1, double z1){
        x = x1;
        y = y1;
        z = z1;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
    
}
